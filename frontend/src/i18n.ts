export type LocaleKey = keyof typeof messages

const detectLocale = (): LocaleKey => {
  const lang = (navigator.language || '').toLowerCase()
  if (lang.startsWith('ru')) return 'ru'
  if (lang.startsWith('zh')) return 'zh'
  return 'en'
}

export const useI18n = () => {
  const locale = ref<LocaleKey>(detectLocale())
  const t = (key: keyof typeof messages.en): string => {
    return messages[locale.value][key] ?? messages.en[key] ?? key
  }
  const localeLabel = computed(() => locale.value.toUpperCase())
  return { locale, localeLabel, t }
}
